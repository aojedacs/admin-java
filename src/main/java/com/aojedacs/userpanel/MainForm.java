    public MainForm() {
        initComponents();
        
        String[] columnas = {"Nombre", "Apellido", "Correo", "Genero", "Direcci√≥n"};
        model = new DefaultTableModel(columnas, 0);
        Set<String> genders = new HashSet<String>();
        
        personTable.setModel(model);
              
        
        
        for(String gender:genders){
            System.out.print(gender);
            jComboBox1.addItem(gender);
        }
        
              
        
        try {
            FileReader reader = new FileReader(route);
            BufferedReader bufferedReader = new BufferedReader(reader);
            
            String linea;
            while ((linea = bufferedReader.readLine()) != null) {
                String user[] = linea.split(",");
                genders.add(user[4]);
                userList.add(new User(user[1],user[2],user[3],user[4],user[5]));
                //System.out.println(linea.split(",")[0]);
                                
            }       

            
            System.out.print(userList);
            reader.close();
        } catch (IOException e) {
            System.out.println("Error al leer el archivo: " + e.getMessage());
        }
        
        
        personTable.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent event) {
                
                if (!event.getValueIsAdjusting()) {
                    int filaSeleccionada = personTable.getSelectedRow();

                
                    if (filaSeleccionada != -1) {
                
                        Object name= personTable.getValueAt(filaSeleccionada, 0);
                        Object lastName = personTable.getValueAt(filaSeleccionada, 1);
                        Object mail = personTable.getValueAt(filaSeleccionada, 2);
                        
                        
                        
                        
                        firstNameInput.setText(name.toString());
                        lastNameInput.setText(lastName.toString());

                        

                        System.out.println("Nombre: " + name + ", Apellido: " + lastName + ", Correo: " + mail);
                    }
                }
            }
        });
        
        
        
        
        
        

    }

